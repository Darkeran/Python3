# Faça um Programa que peça a temperatura em graus Fahrenheit, transforme e mostre a temperatura em graus Celsius. 


fahrenhait = float (input("Insira a temperatura em Fahrenhait:" ))
celsius = 5 *((fahrenhait-32)/9)
print("A temperatura de {}°F esta em fahrenhait, convertendo ela para celsius ficara {}°C".format(fahrenhait, celsius))
